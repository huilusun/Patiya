    public static String fillInEmoji(String mesgXml) {
        if (StringUtils.isBlank(mesgXml)) {
            return null;
        }
        int startindex = mesgXml.indexOf("[@");
        int endindex = mesgXml.indexOf("@]");
        while (startindex != -1 && endindex != -1) {
            String emoji = mesgXml.substring(startindex + 2, endindex);
            mesgXml = mesgXml.replaceAll("\\[@" + emoji + "@\\]",
                    new String(parseHexStr2ByteArray(emoji.replaceAll("0x", "")), Charset.forName("utf-8")));
            startindex = mesgXml.indexOf("[@");
            endindex = mesgXml.indexOf("@]");
        }
        return mesgXml;
    }

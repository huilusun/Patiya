    /**
     * 过滤emoji 或者 其他非文字类型的字符
     * 
     * @param source
     * @return
     */
    public static String filterEmoji(String source) {
        if (StringUtils.isNotBlank(source)) {
            try {
                byte[] bs = source.getBytes(Charset.forName("utf-8"));
                String hex = parseByteArray2HexStr(bs);
                // log.info("utf-8 hex:" + hex);
                hex = hex.toLowerCase();

                for (Iterator<String> iter = emojiMap.keySet().iterator(); iter.hasNext();) {
                    String key = iter.next();
                    hex = hex.replaceAll(key, emojiMap.get(key));
                }
                String body = new String(
                        parseHexStr2ByteArray(hex.toLowerCase().replaceAll(" ", "").replaceAll("0x", "")),
                        Charset.forName("utf-8"));
                return body;
            } catch (Exception e) {
                log.error("emoji 过滤失败。", e);
            }
        }
        return "";
    }

    /**
     * 2进制数字转换为16进制字符串
     * 
     * @param data
     * @return
     */
    public static String parseByteArray2HexStr(byte[] data) {
        if (data == null || data.length < 1) {
            return null;
        }
        StringBuffer hex = new StringBuffer();
        for (int i = 0; i < data.length; i++) {
            hex.append("0X");
            int h = data[i] & 0XFF;
            if (h < 16) {
                hex.append("0");
            }
            hex.append(Integer.toHexString(h) + " ");
        }
        return hex.toString();
    }

    /**
     * 16进制字符串转换为2进制数字
     * 
     * @param hex
     * @return
     */
    public static byte[] parseHexStr2ByteArray(String hex) {
        if (hex == null || "".equals(hex)) {
            return null;
        }
        int length = hex.length() >> 1;
        byte[] data = new byte[length];
        for (int i = 0; i < length; i++) {
            int n = i << 1;
            int height = Integer.valueOf(hex.substring(n, n + 1), 16);
            int low = Integer.valueOf(hex.substring(n + 1, n + 2), 16);
            data[i] = (byte) (height * 16 + low);
        }
        return data;
    }


    protected static String parseBody(String body) {
        body = StringEscapeUtils.unescapeXml(body);
        body = body.replace('&', '@'); // &符号涉及entity解析，可能出现在url中，因此先去除
        SAXReader reader = new SAXReader();
        Document d;
        try {
            d = reader.read(new StringReader(body));
        } catch (DocumentException e) {
            return "";
        }
        Element el = d.getRootElement().element("txt");
        if (el == null) {
            return "";
        }
        return el.getText();
    }

